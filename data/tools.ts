export function filterToolsByCatalog(
  list: Tool[],
  publisherId: string,
  gradeId: string,
  subjectId: string,
): Tool[] {
  return list.filter(
    (t) =>
      t.publisherId === publisherId &&
      (gradeId === "all" || t.gradeId === gradeId) &&
      t.subjectId === subjectId,
  );
}

export function getToolById(id: string): Tool | undefined {
  return tools.find((t) => t.id === id);
}
